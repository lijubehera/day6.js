// Unary Operators in JavaScript
// Initial values
let a = 5;
let b = 2;

// Display initial values
console.log("a = ", a, " & b = ", b);

// Increment using a = a + 1
a = a + 1;
console.log("After a = a + 1 ➡️", a);  // Output: 6

// Using ++ (post-increment)
a++;
console.log("After a++ ➡️", a);  // Output: 7

// Using -- (post-decrement)
b--;
console.log("After b-- ➡️", b);  // Output: 1

// Unary plus to convert string to number
let str = "10";
console.log("Unary + on string '10' ➡️", +str);  // Output: 10

// Unary minus to negate value
let num = 4;
console.log("Unary - on 4 ➡️", -num);  // Output: -4
